# Combine all technical terms for easy lookup
ALL_TECHNICAL_TERMS = (
    PROGRAMMING_LANGUAGES | FRAMEWORKS_LIBRARIES | CLOUD_PLATFORMS | AWS_SERVICES |
    DATABASES | DEVOPS_TOOLS | VERSION_CONTROL | AI_ML_TECHNOLOGIES |
    METHODOLOGIES | TECHNICAL_CONCEPTS | SECURITY_CONCEPTS
)

# All skill-related terms
ALL_SKILLS = ALL_TECHNICAL_TERMS | SOFT_SKILLS

# All qualification-related terms
ALL_QUALIFICATIONS = EDUCATION_CERTIFICATIONS | EXPERIENCE_LEVELS

def is_technical_term(term):
    """Check if a term is a technical skill/tool/technology"""
    return term.lower() in ALL_TECHNICAL_TERMS

def is_soft_skill(term):
    """Check if a term is a soft skill"""
    return term.lower() in SOFT_SKILLS

def is_qualification(term):
    """Check if a term is a qualification/education/certification"""
    return term.lower() in ALL_QUALIFICATIONS

def is_noise_word(term):
    """Check if a term should be filtered out as noise"""
    return term.lower() in NOISE_WORDS

def get_keyword_category(term):
    """Categorize a keyword into technical, soft skill, qualification, or job function"""
    term_lower = term.lower()
    
    if term_lower in ALL_TECHNICAL_TERMS:
        return 'technical'
    elif term_lower in SOFT_SKILLS:
        return 'soft_skill'
    elif term_lower in ALL_QUALIFICATIONS:
        return 'qualification'
    elif term_lower in JOB_FUNCTIONS:
        return 'job_function'
    else:
        return 'other'